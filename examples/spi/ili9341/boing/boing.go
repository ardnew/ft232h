package main

import (
	"log"
	"time"

	"github.com/ardnew/ft232h"
	"github.com/ardnew/ft232h/drv/ili9341"
)

func main() {

	var (
		ft  *ft232h.FT232H
		lcd *ili9341.ILI9341
		err error
	)

	ft, err = ft232h.NewFT232H()
	if nil != err {
		log.Fatalf("NewFT232H(): %s", err)
	}
	defer ft.Close()
	log.Printf("%s", ft)

	lcd, err = ili9341.NewILI9341(ft, &ili9341.Config{
		PinCS:  ft232h.D(3),
		PinDC:  ft232h.C(0),
		PinRST: ft232h.C(4),
		Rotate: ili9341.RotLeft,
	})
	if err != nil {
		log.Fatalf("NewILI9341(): %+v", err)
	}

	if err := boing(lcd); nil != err {
		log.Fatalf("boing(): %+v", err)
	}
}

func boing(lcd *ili9341.ILI9341) error {

	// basic code taken from tinygo project:
	//   https://github.com/tinygo-org/drivers/blob/master/examples/ili9341/pyportal_boing/main.go

	const (
		YBottom float32 = 123.0 // Ball Y coord at bottom
		YBounce float32 = -3.5  // Upward velocity on ball bounce
	)

	type coord struct {
		x float32
		y float32
	}

	var (
		screenWidth = lcd.Size().Width
		//screenHeight = lcd.Size().Height
		frameBuffer = [(BallHeight + 8) * (BallWidth + 8)]uint16{}

		bgColor    = ili9341.LightGrey
		gridColor  = ili9341.DarkGrey
		bgShadow   = ili9341.RGB16(0x5285).Unpack()
		gridShadow = ili9341.RGB16(0x600C).Unpack()
		ballColor1 = ili9341.Red
		ballColor2 = ili9341.White

		startTime int64
		frame     int64

		ballCurr  coord
		ballPrev  coord
		ballVel   coord
		ballFrame float32

		// Color table for ball rotation effect
		palette [16]uint16
	)

	// draw the initial background
	err := lcd.DrawBitmapRect1BPP(gridColor, bgColor, 0, 0, 320, 240, Background[:])
	if nil != err {
		return err
	}

	ballCurr = coord{20.0, YBottom}
	ballVel = coord{0.8, YBounce}
	ballFrame = 3 // Ball animation frame #

	startTime = time.Now().UnixNano()

	for {

		ballPrev = ballCurr
		ballCurr.x += ballVel.x
		ballCurr.y += ballVel.y

		ballVel.y += 0.06 // Update Y velocity
		if (ballCurr.x <= 15.0) || (ballCurr.x >= float32(screenWidth-BallWidth)) {
			ballVel.x *= -1.0 // Left/right bounce
		}
		if ballCurr.y >= YBottom { // Hit ground?
			ballCurr.y, ballVel.y = YBottom, YBounce
		}

		// Determine screen area to update.  This is the bounds of the ball's
		// prior and current positions, so the old ball is fully erased and new
		// ball is fully drawn.
		var minX, minY, maxX, maxY, width, height int

		// Determine bounds of prior and new positions
		minX = int(ballCurr.x)
		if int(ballPrev.x) < minX {
			minX = int(ballPrev.x)
		}
		minY = int(ballCurr.y)
		if int(ballPrev.y) < minY {
			minY = int(ballPrev.y)
		}
		maxX = int(ballCurr.x+BallWidth) - 1
		if int(ballPrev.x+BallWidth)-1 > maxX {
			maxX = int(ballPrev.x+BallWidth) - 1
		}
		maxY = int(ballCurr.y + BallHeight)
		if int(ballPrev.y+BallHeight) > maxY {
			maxY = int(ballPrev.y + BallHeight)
		}

		width = maxX - minX + 1
		height = maxY - minY + 2

		// Ball animation frame # is incremented opposite the ball's X velocity
		ballFrame -= ballVel.x * 2.0
		if ballFrame < 0 {
			ballFrame += 14 // Constrain from 0 to 13
		} else if ballFrame >= 14 {
			ballFrame -= 14
		}

		// Set 7 palette entries to white, 7 to red, based on frame number.
		// This makes the ball spin
		for i := 0; i < 14; i++ {
			if (int(ballFrame)+i)%14 < 7 {
				palette[i+2] = uint16(ballColor1.Pack())
			} else {
				palette[i+2] = uint16(ballColor2.Pack())
			} // Palette entries 0 and 1 aren't used (clear and shadow, respectively)
		}

		// Only the changed rectangle is drawn into the 'renderbuf' array...
		var c uint16                 //, *destPtr;
		bx := minX - int(ballCurr.x) // X relative to ball bitmap (can be negative)
		by := minY - int(ballCurr.y) // Y relative to ball bitmap (can be negative)
		gx := minX                   // X relative to background bitmap (>= 0)
		gy := minY                   // Y relative to background bitmap (>= 0)
		var bk, gk int               // Loop counters and working vars
		//var p uint8                  // 'packed' value of 2 ball pixels
		//var bufIdx int8 = 0

		for y := 0; y <= int(height); y++ { // For each row...

			bk = bx // Need to keep the original bx and gx values,
			gk = gx // so copies of them are made here (and changed in loop below)

			for x := 0; x <= int(width); x++ {

				bg := Background[(gy*screenWidth+gk)/8] & uint8(0x80>>uint(gk&0x07))

				if (bk >= 0) && (bk < BallWidth) && // Is current pixel row/column
					(by >= 0) && (by < BallHeight) { // inside the ball bitmap area?

					// Yes, do ball compositing math...
					c = uint16(Ball[(by*BallWidth+bk)/2]) // Get packed value (2 pixels)
					if 0 == (bk & 1) {
						c >>= 4
					}
					c &= 0xF

					switch c {
					case 0:
						if bg > 0 {
							c = uint16(gridColor.Pack())
						} else {
							c = uint16(bgColor.Pack())
						}
					case 1:
						if bg > 0 {
							c = uint16(gridShadow.Pack())
						} else {
							c = uint16(bgShadow.Pack())
						}
					default:
						c = palette[c]
					}

				} else { // Outside ball bitmap, just draw background bitmap...

					if bg > 0 {
						c = uint16(gridColor.Pack())
					} else {
						c = uint16(bgColor.Pack())
					}

				}

				frameBuffer[y*(width)+x] = c
				bk++ // Increment bitmap position counters (X axis)
				gk++
			}

			by++ // Increment bitmap position counters (Y axis)
			gy++
		}

		err := lcd.DrawBitmapRect16BPP(minX, minY, width-1, height-1, frameBuffer[:width*height])
		if nil != err {
			return err
		}

		// Show approximate frame rate
		frame++
		if frame&0xFF == 0 { // Every 256 frames...
			elapsed := (time.Now().UnixNano() - startTime) / int64(time.Second)
			if elapsed > 0 {
				log.Printf("%.2f fps", float64(frame)/float64(elapsed))
			}
		}
	}

	return nil
}

